// Input and output with the terminal in C

// Include the IO library
// Input is with prinf()
// Output is with scanf()
// Use %d, %s, %f, and other format specifiers to format an input or output
// In C, "" indicates string and '' indicates char

// Example
#include <cstdio>
int main() {
    printf("Hello! Enter a number: ");
    int n;
    scanf("%d", &n);
    printf("You have entered %d.", n);
    return 0;
}

